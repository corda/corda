#ifndef NODE_H
#define NODE_H

#include "uncopyable.h"

template<class T1, class T2>
class Node: private Uncopyable
{
public:
    const T1 key;
    const T2 value;

    Node<T1, T2> * next;

    Node();
    Node(const T1& key, const T2& value);

    bool InsertNext(Node<T1, T2>* p);

    Node<T1, T2>* Find(const T1& key);
    Node<T1, T2>* Remove(const T1& key);
};

#endif // NODE_H 
